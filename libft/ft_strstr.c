#include "./libft.h"

char	*ft_strstr(const char *s, const char *to_find)
{
	int		i;
	int		j;
	char	*str;

	str = (char *)s;
	if (to_find[0] == '\0')
		return (str);
	i = 0;
	while (str[i])
	{
		if (str[i] == to_find[0])
		{
			j = 1;
			while (to_find[j] && str[i + j] == to_find[j])
				j++;
			if (to_find[j] == '\0')
				return (&str[i]);
		}
		i++;
	}
	return (0);
}
