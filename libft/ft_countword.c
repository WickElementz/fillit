int		ft_countword(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while ((str[i] == ' ' || str[i] == '\n' || str[i] == '\t') && str[i])
		i++;
	while (str[i])
	{
		while (str[i] != ' ' && str[i] != '\n' && str[i] != '\t' && str[i])
			i++;
		while ((str[i] == ' ' || str[i] == '\n' || str[i] == '\t') && str[i])
			i++;
		j++;
	}
	return (j);
}
