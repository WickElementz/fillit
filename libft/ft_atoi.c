#include "./libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		number;

	i = 0;
	number = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number *= 10;
		number += (str[i] - 48);
		i++;
	}
	if (neg == 1)
		return (-number);
	else
		return (number);
}
