#include "./libft.h"

void	ft_putstr(char const *s)
{
	char	*str;
	int		i;

	if (!(str = (char *)s))
		return ;
	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}
